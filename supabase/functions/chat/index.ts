import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    // Medical AI system prompt with safety disclaimers
    const systemPrompt = `You are HealthBot, an AI healthcare assistant designed to provide general health information and guidance.

CRITICAL RULES:
1. You are NOT a licensed medical professional and cannot provide medical diagnoses
2. ALWAYS include this disclaimer in your first response: "I'm an AI health assistant and not a doctor. This information is for general guidance only."
3. For urgent symptoms (chest pain, difficulty breathing, severe bleeding, etc.), IMMEDIATELY advise calling emergency services
4. Never prescribe medications or provide specific treatment plans
5. Always recommend consulting a qualified healthcare professional for any medical concerns
6. Be empathetic, clear, and supportive in your responses
7. If asked about symptoms, provide general information but emphasize the need for professional evaluation

CAPABILITIES:
- General health information and wellness tips
- Symptom understanding (not diagnosis)
- Healthy lifestyle guidance
- When to seek medical attention
- Medical terminology explanations

Remember: Your role is to provide helpful information and guide users toward appropriate medical care when needed.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again later." }),
          {
            status: 429,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI credits exhausted. Please contact support." }),
          {
            status: 402,
            headers: { ...corsHeaders, "Content-Type": "application/json" },
          }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI service error" }),
        {
          status: 500,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat error:", error);
    return new Response(
      JSON.stringify({ 
        error: error instanceof Error ? error.message : "Unknown error" 
      }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
